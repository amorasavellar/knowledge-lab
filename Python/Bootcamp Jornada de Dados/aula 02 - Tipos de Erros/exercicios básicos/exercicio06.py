# 6. Escreva um programa que receba dois números flutuantes e realize sua adição.
primeiro_numero = float(input("Digite um número decimal (Ex: 6.21): "))
segundo_numero = float(input("Digite outro número decimal (Ex: 6.21): "))

soma = primeiro_numero + segundo_numero

print(f"A soma entre os números {primeiro_numero} e {segundo_numero}, é: {soma}")