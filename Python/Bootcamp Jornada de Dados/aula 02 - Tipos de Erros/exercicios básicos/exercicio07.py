# 7. Crie um programa que calcule a média de dois números flutuantes fornecidos pelo usuário.
primeiro_numero = float(input("Digite um número decimal (Ex: 2.40): "))
segundo_numero = float(input("Digite outro número decimal (Ex: 2.40): "))

media = (primeiro_numero + segundo_numero)//2

print(f"A média entre os números {primeiro_numero} e {segundo_numero}, é {media}")